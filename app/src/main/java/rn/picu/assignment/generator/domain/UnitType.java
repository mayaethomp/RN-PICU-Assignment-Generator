package rn.picu.assignment.generator.domain;

/**
 * Represents units of the hospitals that nurses are hired to and work in on a
 * regular basis.
 */
public enum UnitType {
  /**
   * Pediatric Intesive Care Unit
   */
  PICU,

  /**
   * Newborn and Infant Intensive Critical Care Unit
   */
  NICCU,

  /**
   * Cardiothoracic Intesive Care Unit
   */
  CTICU,

  /**
   * Float Team
   */
  FLOAT
}
